#include <stdio.h>

int main() {
    printf("Letras maiusculas:\n");
    printf("A: %i\n", 'A');
    printf("B: %i\n", 'B');
    printf("C: %i\n", 'C');
    printf("\nLetras minusculas:\n");
    printf("a: %i\n", 'a');
    printf("b: %i\n", 'b');
    printf("c: %i\n", 'c');
    printf("\nNumeros e caracteres especiais:\n");
    printf("0: %i\n", '0');
    printf("1: %i\n", '1');
    printf("2: %i\n", '2');
    printf("$: %i\n", '$');
    printf("*: %i\n", '*');
    printf("+: %i\n", '+');
    printf("/: %i\n", '/');
    printf("Espaco em branco: %i\n", ' ');
    return 0;
}
